"""Contenu fidèle de la Convention d'adhésion-cadre tripartite LOGI'SCOP Mode D V1.2 (2026-07-20)."""

CONVENTION_TITLE = ("CONVENTION D'ADHÉSION-CADRE TRIPARTITE LOGI'SCOP DE TRANSPORT PUBLIC "
                    "ROUTIER DE MARCHANDISES — MODE D")
CONVENTION_SUBTITLE = ("O'SCOP / LOGI'SCOP TRANSPORTEUR CONTRACTANT — DONNEUR D'ORDRE — "
                       "TRANSPORTEUR EXÉCUTANT · Version V1.2 du 20/07/2026")

ARTICLES = [
    ("ARTICLE 1 — DÉFINITIONS",
     "Sont définis pour l'application de la présente Convention : le Dashboard, le Donneur d'Ordre, l'Envoi, "
     "l'Expéditeur, le Destinataire, l'Ordre de Transport, le POD/ePOD, la Marchandise, le Transport sous "
     "température dirigée, la Prestation annexe, l'Incident critique, la Valeur déclarée, l'Intérêt spécial à la "
     "livraison, le FOGEDOM-SCIC, le Transporteur Contractant et le Transporteur Exécutant. Ces définitions "
     "garantissent une compréhension commune et uniforme des obligations et concepts de la Convention."),
    ("ARTICLE 2 — OBJET ET CHAMP D'APPLICATION",
     "La Convention établit les règles générales d'organisation, de commande, d'exécution, de suivi et de "
     "règlement des opérations de transport et de coordination logistique via LOGI'SCOP. Le périmètre par défaut "
     "couvre le transport routier intérieur, le groupage, la distribution et le dernier kilomètre ; les segments "
     "spécialisés relèvent d'annexes spécifiques. Sont exclus, sauf accord écrit et vérification des habilitations : "
     "certaines marchandises dangereuses, déchets, animaux, fonds et valeurs, armes, stupéfiants, produits "
     "illicites, matières radioactives et marchandises soumises à autorisation spéciale."),
    ("ARTICLE 3 — QUALIFICATION DE LOGI'SCOP COMME TRANSPORTEUR CONTRACTANT",
     "LOGI'SCOP est qualifié en MODE D comme cocontractant transporteur du Donneur d'Ordre : il agit en son nom "
     "propre et non comme bourse de fret ou intermédiaire. Il s'engage à prendre en charge, déplacer et livrer la "
     "Marchandise, principalement avec ses propres moyens. La location avec conducteur est possible sous "
     "conditions ; la sous-traitance n'est admise que si le tiers dispose des qualités réglementaires requises ou "
     "en cas d'exception applicable. Le recours structurel ou habituel à la sous-traitance est exclu. LOGI'SCOP "
     "vérifie et archive ses autorisations et celles des transporteurs exécutants avant chaque mission."),
    ("ARTICLE 4 — OBLIGATIONS D'O'SCOP AU TRAVERS DE LOGI'SCOP, TRANSPORTEUR CONTRACTANT",
     "LOGI'SCOP conclut les contrats de transport, prend en charge ou fait prendre en charge la Marchandise et "
     "organise son déplacement. Il exécute la mission avec ses propres moyens et ne recourt à des tiers que dans "
     "les cas légalement autorisés et documentés. Il émet les documents de transport, maintient ses licences et "
     "assurances, facture le Donneur d'Ordre et répond des pertes, avaries ou retards. LOGI'SCOP administre le "
     "Dashboard et la traçabilité."),
    ("ARTICLE 5 — OBLIGATIONS DU DONNEUR D'ORDRE",
     "Le Donneur d'Ordre communique une description exacte et complète de l'Envoi (nature, quantité, poids, "
     "valeur, emballage, dangers, température, contraintes). Il met à disposition marchandises, documents et "
     "personnes nécessaires, s'assure du respect des obligations par l'expéditeur et le destinataire et ne "
     "dissimule aucune marchandise dangereuse. Il fournit les documents sanitaires et douaniers requis, indique "
     "toute valeur déclarée ou intérêt spécial, règle les prestations dans les délais et coopère en cas d'incident."),
    ("ARTICLE 6 — OBLIGATIONS ET ÉLIGIBILITÉ DU TRANSPORTEUR EXÉCUTANT",
     "Le Transporteur Exécutant maintient inscriptions, licences, capacités et assurances et les communique à "
     "LOGI'SCOP. Il affecte des conducteurs habilités et des véhicules adaptés et sûrs, exécute strictement "
     "l'Ordre de LOGI'SCOP, vérifie l'état apparent de l'Envoi, protège la Marchandise et signale tout incident "
     "ou retard. Il ne transborde ni ne sous-traite sans autorisation et remet les preuves de collecte, livraison "
     "et température dans les délais."),
    ("ARTICLE 7 — FORMATION DES ORDRES DE TRANSPORT",
     "Le Donneur d'Ordre soumet sa demande à LOGI'SCOP qui émet un Ordre de Transport ; la validation "
     "électronique de l'Ordre par les deux parties forme le contrat d'application. Si un tiers intervient, l'Ordre "
     "identifie le mode d'exécution et son fondement juridique. Aucune mission n'est acceptée sur la seule base "
     "d'une géolocalisation ou d'une proposition automatique. Toute modification substantielle (Marchandise, prix, "
     "lieux, température, délai, mode, valeur) exige une nouvelle validation. En urgence, LOGI'SCOP peut donner "
     "des instructions orales confirmées ensuite."),
    ("ARTICLE 8 — DÉCLARATION DE LA MARCHANDISE",
     "Le Donneur d'Ordre répond des conséquences d'une déclaration inexacte ou incomplète, sous réserve des "
     "contrôles raisonnables de LOGI'SCOP et du Transporteur Exécutant. Le poids déclaré doit être vérifiable ; "
     "toute surcharge ou incompatibilité peut entraîner le refus de l'Envoi. Les marchandises dangereuses sont "
     "acceptées sous conditions strictes ; les marchandises de grande valeur ou sensibles font l'objet de "
     "consignes particulières."),
    ("ARTICLE 9 — CONDITIONNEMENT, EMBALLAGE, ÉTIQUETAGE ET SUPPORTS",
     "Le Donneur d'Ordre ou l'Expéditeur conditionne et étiquette la Marchandise de manière à supporter les "
     "contraintes du transport et permettre une manutention sécurisée. Le Transporteur Exécutant signale les "
     "défauts apparents ; l'absence de réserve ne vaut pas reconnaissance d'un vice non décelable. L'échange ou "
     "la consignation d'emballages réutilisables ne s'applique que si l'Ordre le prévoit."),
    ("ARTICLE 10 — CHARGEMENT, CALAGE, ARRIMAGE ET DÉCHARGEMENT",
     "L'Ordre de Transport désigne la partie responsable de ces opérations ; à défaut, les règles du contrat type "
     "réglementaire s'appliquent. La partie qui opère respecte les règles de sécurité et de compatibilité. Le "
     "conducteur peut interrompre une opération dangereuse ou impraticable. Le Transporteur Exécutant vérifie que "
     "le chargement ne compromet pas la sécurité."),
    ("ARTICLE 11 — DÉLAIS, RENDEZ-VOUS, ATTENTE ET IMMOBILISATION",
     "Les fenêtres de collecte et de livraison, les délais de route et le caractère essentiel des rendez-vous sont "
     "précisés dans l'Ordre. Les temps d'attente sont rémunérés selon l'Annexe 2 ; début et fin d'attente sont "
     "établis par horodatage ou éléments concordants. Le Transporteur Exécutant alerte LOGI'SCOP de tout retard "
     "prévisible. Le Donneur d'Ordre ne peut déduire unilatéralement des pénalités."),
    ("ARTICLE 12 — HYGIÈNE ALIMENTAIRE ET TEMPÉRATURE DIRIGÉE",
     "Les véhicules sont propres et aptes à maintenir les températures prescrites. Le Donneur d'Ordre indique la "
     "température de consigne, la tolérance et les besoins de pré-refroidissement. Le Transporteur Exécutant "
     "vérifie l'état et le fonctionnement du groupe frigorifique. Les températures sont enregistrées selon "
     "l'Annexe 4 et les données brutes conservées. Toute rupture de température constitue un Incident critique."),
    ("ARTICLE 13 — TRAÇABILITÉ, ePOD, DASHBOARD ET PREUVE",
     "Le Dashboard affiche ordres, véhicules, conducteurs, étapes, statuts, preuves, températures et incidents ; "
     "chaque événement est horodaté. Le Transporteur Exécutant transmet le POD/ePOD à LOGI'SCOP dans les 2 heures "
     "suivant la livraison. Journaux, signatures électroniques, ePOD, données GPS et documents constituent des "
     "éléments de preuve, conservés pendant 5 ans."),
    ("ARTICLE 14 — PRIX, PRESTATIONS ANNEXES ET INDEXATION ÉNERGÉTIQUE",
     "Le prix de base et ses unités sont fixés à l'Annexe 2 ou dans l'Ordre. Le prix rémunère les prestations "
     "effectivement accomplies, les temps d'attente, les sujétions particulières et les péages. La part des "
     "charges de produits énergétiques est identifiée et révisée selon une formule convenue. Les frais "
     "extraordinaires non prévisibles sont dus après information et justification. Les tarifs sont révisés "
     "annuellement."),
    ("ARTICLE 15 — FACTURATION DU FRET, PAIEMENT ET ACTION DIRECTE",
     "LOGI'SCOP facture au Donneur d'Ordre le prix intégral du transport, les prestations annexes et l'indexation "
     "énergétique ; en cas de sous-traitance, le Transporteur Exécutant facture LOGI'SCOP. Les factures sont "
     "réglées dans le délai légal et mentionnent références d'Ordre, dates et trajets. La compensation avec un "
     "dommage allégué est interdite sans accord écrit. L'action directe du voiturier effectif est reconnue."),
    ("ARTICLE 16 — EXÉCUTION EN PROPRE, LOCATION AVEC CONDUCTEUR, SOUS-TRAITANCE EXCEPTIONNELLE ET MULTIMODAL",
     "LOGI'SCOP organise le groupage et demeure responsable de l'hygiène, de la température, de la sûreté, du "
     "délai et de la traçabilité. L'exécution en propre est le mode normal. La location avec conducteur est "
     "possible, le loueur n'étant pas cocontractant principal. La sous-traitance n'est admise que si LOGI'SCOP "
     "justifie de la qualité de commissionnaire ou d'une exception réglementaire. Les segments maritimes, aériens, "
     "internationaux ou multimodaux sont identifiés dans l'Ordre."),
    ("ARTICLE 17 — LIVRAISON, RÉSERVES ET RÉCLAMATIONS",
     "Le Destinataire vérifie l'état apparent de la Marchandise avant de signer le POD ; les réserves sont "
     "précises et motivées. En cas de dommage non apparent, les droits s'exercent dans les délais du régime "
     "applicable. Le réclamant conserve la marchandise et les preuves. LOGI'SCOP accuse réception des "
     "réclamations et transmet sa position documentée."),
    ("ARTICLE 18 — RESPONSABILITÉ DU TRANSPORTEUR CONTRACTANT ET DU TRANSPORTEUR EXÉCUTANT",
     "LOGI'SCOP répond envers le Donneur d'Ordre de la perte, de l'avarie ou du retard, y compris lorsque "
     "l'exécution est confiée à un tiers. Le Transporteur Exécutant répond envers LOGI'SCOP de ses faits et de "
     "ceux de ses préposés. Le Donneur d'Ordre répond des conséquences de ses instructions et déclarations. "
     "LOGI'SCOP conserve ses recours contre le Transporteur Exécutant. Plafonds et causes d'exonération résultent "
     "du contrat type applicable ; dommages indirects et pertes de marge sont exclus."),
    ("ARTICLE 19 — DÉCLARATION DE VALEUR ET INTÉRÊT SPÉCIAL À LA LIVRAISON",
     "Le Donneur d'Ordre peut solliciter une déclaration de valeur ou un intérêt spécial avant l'acceptation de "
     "la mission ; LOGI'SCOP peut l'accepter moyennant un supplément de prix. La déclaration acceptée figure dans "
     "l'Ordre et le document de transport. Une simple indication de valeur sur une facture ne vaut pas "
     "déclaration de valeur acceptée."),
    ("ARTICLE 20 — ASSURANCES DE LOGI'SCOP ET DU TRANSPORTEUR EXÉCUTANT",
     "LOGI'SCOP maintient une assurance de responsabilité contractuelle de transporteur couvrant l'exécution en "
     "propre, la location avec conducteur et la sous-traitance. Le Transporteur Exécutant maintient une assurance "
     "de responsabilité contractuelle de voiturier et une RC exploitation. Le Donneur d'Ordre peut souscrire une "
     "assurance facultative. Les attestations d'assurance sont déposées avant chaque mission."),
    ("ARTICLE 21 — INCIDENTS, VOLS, RAPPELS ET CONTINUITÉ",
     "Tout Incident critique est signalé immédiatement. Le Transporteur Exécutant protège les personnes, sécurise "
     "le véhicule et informe LOGI'SCOP. En cas de vol, disparition ou effraction, il dépose plainte. Les retraits "
     "et rappels sont coordonnés. LOGI'SCOP peut activer un plan de continuité ; coûts et responsabilités sont "
     "attribués selon la cause."),
    ("ARTICLE 22 — NIVEAUX DE SERVICE, PLANS CORRECTIFS ET AVOIRS DE SERVICE",
     "Les indicateurs et seuils de performance figurent à l'Annexe 5 (ponctualité, conformité température, ePOD, "
     "avaries…). Un passage au statut orange déclenche une analyse ; un statut rouge répété peut suspendre les "
     "missions. Des avoirs de service peuvent être prévus. Les sanctions punitives sont exclues."),
    ("ARTICLE 23 — FOGEDOM-SCIC ET RÉSILIENCE LOGISTIQUE",
     "Le FOGEDOM-SCIC peut soutenir des mesures de continuité logistique (recherche d'un transporteur de "
     "substitution, cross-dock…). Son intervention dépend des règles de gouvernance et des ressources "
     "disponibles. Le FOGEDOM-SCIC n'est ni une assurance, ni une garantie de livraison ou de paiement."),
    ("ARTICLE 24 — FORCE MAJEURE ET IMPRÉVISION",
     "La force majeure produit les effets prévus par la loi ; les parties affectées s'informent mutuellement et "
     "fournissent des justificatifs. Pour les denrées périssables, une solution de sauvegarde est déterminée. "
     "Lorsqu'un changement imprévisible rend l'exécution onéreuse, une renégociation est possible."),
    ("ARTICLE 25 — CONFORMITÉ, DOUANES, SANCTIONS ET LOYAUTÉ",
     "Chaque partie respecte les réglementations du transport, de la sécurité, du travail, de l'hygiène, de la "
     "concurrence et de la lutte contre la corruption. Le Donneur d'Ordre fournit les informations douanières. "
     "LOGI'SCOP et le Transporteur Exécutant garantissent l'absence de travail dissimulé. Toute manipulation de "
     "prix, fausse déclaration ou fraude au fret entraîne suspension et peut être signalée aux autorités."),
    ("ARTICLE 26 — CONFIDENTIALITÉ, DONNÉES ET PROPRIÉTÉ INTELLECTUELLE",
     "Prix, clients, itinéraires, marges, procédures, données de géolocalisation, incidents et documents sont "
     "confidentiels. Les accès au Dashboard sont nominatifs et limités. Une annexe RGPD précise les rôles et "
     "obligations. Les données GPS et conducteurs sont collectées pour la mission, la sécurité et la preuve. "
     "O'SCOP peut utiliser des données agrégées pour les indicateurs ESS."),
    ("ARTICLE 27 — AUDIT ET CONTRÔLE",
     "Le Donneur d'Ordre peut vérifier licences, assurances et procédures de LOGI'SCOP ; LOGI'SCOP dispose du "
     "même droit sur le Transporteur Exécutant. L'audit respecte la confidentialité et ne perturbe pas "
     "l'activité. Un écart majeur entraîne un plan correctif ou une résiliation."),
    ("ARTICLE 28 — DURÉE, RENOUVELLEMENT ET FIN",
     "La Convention est conclue pour une durée initiale de douze mois, renouvelable par périodes de douze mois "
     "sauf dénonciation avec préavis. Chaque partie peut résilier en cas de manquement substantiel. La "
     "résiliation immédiate est possible en cas de perte de licence, d'absence d'assurance, de fraude, de danger "
     "grave, de travail dissimulé ou d'insolvabilité. Les Ordres acceptés avant la fin sont exécutés."),
    ("ARTICLE 29 — CESSION ET CHANGEMENT DE CONTRÔLE",
     "Aucune partie ne cède la Convention sans accord écrit des deux autres, sauf restructuration intragroupe. "
     "Tout changement de contrôle, fusion ou cession d'activité affectant les licences ou assurances est notifié."),
    ("ARTICLE 30 — NOTIFICATIONS",
     "Les coordonnées contractuelles et d'urgence figurent à l'Annexe 1. Les mises en demeure, résiliations et "
     "réclamations formelles sont adressées par un moyen établissant l'identité, le contenu et la date "
     "d'envoi/réception. Les alertes du Dashboard complètent mais ne remplacent pas une notification formelle."),
    ("ARTICLE 31 — HIÉRARCHIE CONTRACTUELLE, PREUVE ET SIGNATURE ÉLECTRONIQUE",
     "L'ordre de priorité des documents est : avenants, Convention, annexes, Ordre de Transport accepté, document "
     "de transport, conditions générales. Le contrat type réglementaire s'applique aux matières non réglées. La "
     "Convention, les Ordres et autres documents peuvent être signés électroniquement."),
    ("ARTICLE 32 — DROIT APPLICABLE ET LITIGES",
     "La Convention est soumise au droit français. Les parties tentent une négociation de bonne foi avant de "
     "saisir le médiateur. Les actions nées du contrat de transport relèvent des prescriptions spéciales "
     "applicables. Tout litige relèvera de la compétence du Tribunal de Commerce compétent."),
]

